class BangunDatar {

    public double luas(double sisi) {
        return sisi * sisi; // Luas persegi
    }

    public double luas(double panjang, double lebar) {
        return panjang * lebar; // Luas persegi panjang
    }

    public double luas(double alas, double tinggi) {
        return 0.5 * alas * tinggi; // Luas segitiga
    }
}